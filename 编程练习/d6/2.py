a = (1,2,3,4)
print(a)
print(*a)#对a元组进行了拆包

a = (1,2,3)
x, y, z = a#直接指定每个位置变量名称对a进行了解包
print(x,y,z)

a = (1,2,3)
x, y, _ = a
x, _, __ = a
x, _, _ = a
_, __, ___ = a
#所有的下划线都是一个变量，只是不使用
#第12行先将_赋成a的第2项，再赋成第3项
x, *y = a
x, *_ = a#第1项赋成x，其余项带上*号赋成列表
#注意，带*号的解包在式子中只能出现一次

a, b = 1, 2
print(a,b)
b, a = a, b#python特有的原地交换
print(a,b)
